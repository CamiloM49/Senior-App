using System;

namespace CapaDTO
{
    public class Cliente
    {
        private String rut;
        private String nombre;

        public string Rut
        {
            get
            {
                return rut;
            }

            set
            {
                rut = value;
            }
        }

        public string Nombre
        {
            get
            {
                return nombre;
            }

            set
            {
                nombre = value;
            }
        }
    }
}
